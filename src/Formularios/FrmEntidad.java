    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (txtDesc.getText().length()==0){
            JOptionPane.showMessageDialog(null,
            "Debe de seleccionar un registro, para eliminar",
            "Entidad", JOptionPane.INFORMATION_MESSAGE );
        }else{  
            int confirmado = JOptionPane.showConfirmDialog(null,"Desea eliminar esta entidad","Entidad",JOptionPane.YES_NO_OPTION);

            if (JOptionPane.OK_OPTION == confirmado){
                int codigo;
                codigo=Integer.parseInt(lblCodigoEnt.getText());
                Entidad eliminar=new Entidad();

                try{
                    eliminar.sql_EliminarEntidad(codigo);
                    btnNuevo.setEnabled(true);
                    btnModificar.setEnabled(true);
                    btnEliminar.setEnabled(true);
                    btnGuardar.setEnabled(false);
                    btnCancelar.setEnabled(false);
                    txtDesc.setText("");
                    txtAnio.setText("");
                    txtAnio.setEnabled(false);

                    JOptionPane.showMessageDialog(null,
                        "Entidad Eliminada",
                        "Entidad", JOptionPane.INFORMATION_MESSAGE );
                    try{
                        eliminar.ConsultaEntidad(JTMostrar);
                    } catch (SQLException e){
                        System.out.println(e);
                    }    
                }catch (SQLException e){
                    JOptionPane.showMessageDialog(null,
                        "No se puede Eliminar esta Entidad datos asociados",
                        "Entidad", JOptionPane.INFORMATION_MESSAGE );
                    System.out.println(e);
                    btnNuevo.setEnabled(true);
                    btnModificar.setEnabled(true);
                    btnEliminar.setEnabled(true);
                    btnGuardar.setEnabled(false);
                    btnCancelar.setEnabled(false);
                    txtDesc.setText("");
                    txtAnio.setText("");
                    txtAnio.setEnabled(false);
                }
            }else{
                    btnNuevo.setEnabled(true);
                    btnModificar.setEnabled(true);
                    btnEliminar.setEnabled(true);
                    btnGuardar.setEnabled(false);
                    btnCancelar.setEnabled(false);
                    txtDesc.setText("");
                    txtAnio.setText("");
                    txtAnio.setEnabled(false);
                    
        }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

