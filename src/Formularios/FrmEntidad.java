    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Entidad nuevo=new Entidad();
        Integer anio,codigo;
        String descrip;
      
        descrip=txtDesc.getText();
        anio=Integer.parseInt(txtAnio.getText());
        codigo=Integer.parseInt(lblCodigoEnt.getText());
     
     
        try{
            nuevo.sql_IngresarActualizarEntidad(descrip,anio,codigo);
            JOptionPane.showMessageDialog(null,
            "Datos ingresados correctamente",
            "Entidad", JOptionPane.INFORMATION_MESSAGE );
        
            btnNuevo.setEnabled(true);
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnCancelar.setEnabled(false);
            btnSalir.setEnabled(true);
            txtDesc.setText("");
            txtAnio.setText("");
            txtAnio.setEnabled(false);
            txtDesc.setEnabled(false);
            try{
                nuevo.ConsultaEntidad(JTMostrar);
            } catch (SQLException e){
                System.out.println(e);
            }    
        } catch (SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

